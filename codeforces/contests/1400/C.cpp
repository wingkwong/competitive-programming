void solve() {
    // if si=1, it means "either wi−x=1 or wi+x=1," 
    // whereas si=0 means "both wi−x=0 and wi+x=0."
    string s;
    int x;
    cin >> s >> x;
    int n = s.size();
    string ans(n,'1');
    REP(i,n){
        if(i+x<n&&s[i]=='0') ans[i+x]='0';
        if(i>=x&&s[i]=='0') ans[i-x]='0';
    }

    REP(i,n){
        int ok=0;
        if(i+x<n&&ans[i+x]=='1') ok=1;
        if(i>=x&&ans[i-x]=='1') ok=1;
        if(s[i]!=(ok?'1':'0')){
            OUT(-1);
            return;
        }
    }

    OUT(ans);
}

