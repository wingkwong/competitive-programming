class StockSpanner {
public:
    stack<pair<int,int>> s;
    StockSpanner() {}
    
    int next(int price) {
        int res=1;
        // put the price and the number of prices that are less than the current one into a pair
        while(!s.empty()&&s.top().first<=price){
            // update res and pop it 
            res+=s.top().second;
            s.pop();
        }
        s.push({price,res});
        return res;
    }
};

